game.onUpdate(function () {
    if (blockMenu.isMenuOpen()) {
        blockMenu.setControlsEnabled(true)
    } else {
        blockMenu.setControlsEnabled(false)
    }
    if (gameMode == "Playing") {
        if (info.score() < 1) {
            gamePlaying = false
            reinDeer(0)
            sprSantaClause.ay = 50
            sprSantaClause.ax = 30
            sprSantaClause.setFlag(SpriteFlag.AutoDestroy, true)
        } else if (info.score() > 1 && info.score() < 50) {
            reinDeer(1)
            gameApplyMultiplier(1)
        } else if (info.score() > 50 && info.score() < 80) {
            reinDeer(2)
            gameApplyMultiplier(1.5)
        } else if (info.score() > 80 && info.score() < 100) {
            reinDeer(3)
            gameApplyMultiplier(2)
        } else if (info.score() > 105) {
            reinDeer(4)
            gameApplyMultiplier(2.5)
        }
    }
})
